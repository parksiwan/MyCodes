Tetrads::Tetrads(int x, int y, int type, int rotation) 
{
	curX_ = x;
	curY_ = y;
	type_ = type;
	rotation_ = rotation;
	
	for (int i = 0; i < HORIZONTAL; i++)
	{
		for (int j = 0; j < VERTICAL; j++)
			tetrad_[i][j] = tetrads[type_][rotation_][i][j];
	}
}

Tetrads::~Tetrads() 
{

}

void Tetrads::pollEvents(SDL_Event& event) 
{
	if (event.type == SDL_KEYDOWN) 
	{
		switch (event.key.keysym.sym) 
		{
			case SDLK_LEFT:
				curX_ -= 20;
				break;
			case SDLK_RIGHT:
				curX_ += 20;
				break;
			case SDLK_UP:
				if (rotation_ == 3)
					rotation_ = 0;
				else
					rotation_ += 1;
				break;
			case SDLK_DOWN:
				curY_ += 10;
				break;
		}
	} 
}

void Tetrads::moveDown() 
{
	curY_ += 20;
}

void Tetrads::rotateDown()
{
	curY_ += 10;
}
